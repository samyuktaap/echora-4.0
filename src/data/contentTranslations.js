// Helper function to get translated content with better mapping
export const getTranslatedContent = (language, type, id, field) => {
  try {
    // First try to get translation for the specific ID
    const translations = CONTENT_TRANSLATIONS[language];
    if (translations && translations[type] && translations[type][id] && translations[type][id][field]) {
      return translations[type][id][field];
    }
    
    // If not found, try to find by content matching (for dynamic data)
    if (type === 'tasks') {
      const englishTask = CONTENT_TRANSLATIONS.en.tasks[id];
      if (englishTask) {
        // Try to find matching task by comparing original content
        const taskKeys = Object.keys(translations?.tasks || {});
        for (const key of taskKeys) {
          const translatedTask = translations.tasks[key];
          if (translatedTask && translatedTask[field]) {
            return translatedTask[field];
          }
        }
      }
    }
    
    // Fallback to English
    const fallback = CONTENT_TRANSLATIONS.en[type]?.[id]?.[field];
    return fallback || `Missing translation: ${type}.${id}.${field}`;
  } catch (error) {
    console.error('Translation error:', error);
    return `Translation error: ${type}.${id}.${field}`;
  }
};

